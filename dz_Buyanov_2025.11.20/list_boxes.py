# Пользователь вводит целое число N.

# Если число N >= 0, то компьютер строит список boxes,
# в котором лежат N штук списков длиною от 0 до N.
# При этом все вложенные списки заполнены числами N.

# Для N == 0 будет [[]]

# Для N == 1 будет [[], [1]]

# Для N == 2 будет [[], [2], [2, 2]]

# Для N == 3 будет [[], [3], [3, 3], [3, 3, 3]]

# Для N == 4 будет [[], [4], [4, 4], [4, 4, 4], [4, 4, 4, 4]]

# И так далее

# Если число N < 0, то комьютер печатает фразу "Такое значение
# не подходит."


N = input("Ввдите целое число: ")
N = int(N)

